/*створіть інструкцію swith,яка буде виводити "Hello",якщо fruits є "banana" та "Welcome",якщо fruits є "apple":*/

const fruits = "Banana"; //"Apple"другий варіант
switch(fruits){
    case "Banana":
        console.log("Hello");
        break;
    case "Apple":
        console.log("Welcome");
    break;
}
