// оператори присвоєння :
let age = 18 + 5 ;
age += 2 // age = age + 2
age -= 3 // age = age - 2
age *= 2 // age = age * 2
age /= 2 // age = age / 2

age++ // age = age + 1
age-- // age = age - 1
console.log(age)

// оператори порівняння :
const kvitka = 40
console.log(age > kvitka)
console.log(age >= kvitka)
console.log(age < kvitka)
console.log(age <= kvitka)
console.log(age == kvitka)